// ==================== COMBINED DATA ====================
export const allVehicles = [...carsData, ...bikesData];

// ==================== HELPER FUNCTIONS ====================
/**
 * Get unique brands from vehicle data
 * @param {string} type - 'car', 'bike', or 'all'
 * @returns {string[]} Array of unique brand names
 */
export const getBrands = (type = 'all') => {
  const data = type === 'car' ? carsData : type === 'bike' ? bikesData : allVehicles;
  return [...new Set(data.map(v => v.brand))].sort();
};

/**
 * Get unique fuel types from vehicle data
 * @param {string} type - 'car', 'bike', or 'all'
 * @returns {string[]} Array of unique fuel types
 */
export const getFuelTypes = (type = 'all') => {
  const data = type === 'car' ? carsData : type === 'bike' ? bikesData : allVehicles;
  return [...new Set(data.map(v => v.fuelType))].sort();
};

/**
 * Get price range from vehicle data
 * @param {string} type - 'car', 'bike', or 'all'
 * @returns {{ min: number, max: number }}
 */
export const getPriceRange = (type = 'all') => {
  const data = type === 'car' ? carsData : type === 'bike' ? bikesData : allVehicles;
  const prices = data.map(v => v.price);
  return { min: Math.min(...prices), max: Math.max(...prices) };
};

/**
 * Format price in INR format
 * @param {number} price - Price in numbers
 * @returns {string} Formatted price string
 */
export const formatPrice = (price) => {
  if (price >= 10000000) {
    return `₹${(price / 10000000).toFixed(2)} Cr`;
  } else if (price >= 100000) {
    return `₹${(price / 100000).toFixed(2)} L`;
  } else {
    return `₹${price.toLocaleString('en-IN')}`;
  }
};

/**
 * Get featured vehicles
 * @param {string} type - 'car', 'bike', or 'all'
 * @returns {Array} Featured vehicles
 */
export const getFeaturedVehicles = (type = 'all') => {
  const data = type === 'car' ? carsData : type === 'bike' ? bikesData : allVehicles;
  return data.filter(v => v.featured);
};

/**
 * Search vehicles by query
 * @param {string} query - Search query
 * @param {string} type - 'car', 'bike', or 'all'
 * @returns {Array} Matching vehicles
 */
export const searchVehicles = (query, type = 'all') => {
  const data = type === 'car' ? carsData : type === 'bike' ? bikesData : allVehicles;
  const q = query.toLowerCase();
  return data.filter(v =>
    v.name.toLowerCase().includes(q) ||
    v.brand.toLowerCase().includes(q) ||
    v.bodyType.toLowerCase().includes(q) ||
    v.fuelType.toLowerCase().includes(q)
  );
};

/**
 * Get vehicle by ID
 * @param {string} id - Vehicle ID
 * @returns {Object|undefined} Vehicle object
 */
export const getVehicleById = (id) => {
  return allVehicles.find(v => v.id === id);
};
